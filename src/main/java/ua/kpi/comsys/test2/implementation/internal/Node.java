package ua.kpi.comsys.test2.implementation.internal;

/**
 * Внутрішній елемент кільцевого двонаправленого списку.
 * <p>
 * Містить одну цифру числа та посилання на сусідні вузли
 * (попередній та наступний), що дозволяє реалізувати
 * кільцеву двозв'язну структуру даних.
 *
 *
 * @author Андрій Шевчук
 * @group  ІС-31
 * @recordBookNumber 26
 */
public class Node {

    /** Значення цифри, що зберігається у вузлі. */
    public byte value;

    /** Посилання на наступний вузол у списку. */
    public Node next;

    /** Посилання на попередній вузол у списку. */
    public Node prev;

    /**
     * Створює новий вузол з указаною цифрою.
     *
     * @param value цифра, що зберігатиметься в цьому вузлі
     */
    public Node(byte value) {
        this.value = value;
    }
}
